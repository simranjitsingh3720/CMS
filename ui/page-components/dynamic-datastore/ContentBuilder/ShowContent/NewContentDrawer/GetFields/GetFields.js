function GetFields(appearenceType) {
  switch (appearenceType) {
    case 'short':
      return '';

    case 'long':
      return '';

    case 'number':
      return '';

    case 'checkbox':
      return '';

    case 'radio':
      return '';

    case 'tags':
      return '';

    case 'dateAndTime':
      return '';

    case 'switch':
      return '';

    case 'boolean radio':
      return '';

    default:
      return '';
  }
}

export default GetFields;
